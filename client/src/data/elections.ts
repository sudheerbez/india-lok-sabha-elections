export interface PartyResult {
  party: string;
  seats: number;
  voteShare: number;
  isWinner: boolean;
  color: string;
}

export interface StateResult {
  state: string;
  winningParty: string;
  seats: { party: string; count: number }[];
}

export interface CoalitionInfo {
  name: string;
  parties: string[];
  totalSeats: number;
}

export interface Election {
  year: number;
  totalSeats: number;
  majorityMark: number;
  primeMinister: string;
  pmParty: string;
  turnoutPercentage: number | null;
  totalVoters: number | null;
  parties: PartyResult[];
  states: StateResult[];
  coalitions: CoalitionInfo[];
  notes: string;
}

